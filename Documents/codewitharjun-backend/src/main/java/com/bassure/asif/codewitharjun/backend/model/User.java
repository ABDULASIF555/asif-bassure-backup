package com.bassure.asif.codewitharjun.backend.model;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import org.hibernate.annotations.GenericGenerator;

/**
 *
 * @author bas200190
 */
@Entity
public class User {

    @Id
    @GenericGenerator(name = "hibernate-generator", strategy = "org.hibernate.id.UUIDHexGenerator")
    @GeneratedValue(strategy = GenerationType.UUID, generator = "hibernate-generator")
//   @GeneratedValue()
    private String id;
    private String name;
    private String userName;
    private String email;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
